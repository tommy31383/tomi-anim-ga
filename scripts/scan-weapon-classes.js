// Classify every weapon / shield / tool sheet definition into a class:
//   1H, 2H, Ranged, Shield, Tool, Unknown
// Outputs:
//   - WEAPONS_CLASSIFICATION.md (human report)
//   - sources/state/weapon-classes-data.js (consumed by UI badge + random)

import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const SHEET_DEF_DIR = path.join(ROOT, "sheet_definitions");

const ANIM_1H = new Set([
  "combat",
  "idle",
  "1h_slash",
  "1h_backslash",
  "1h_halfslash",
]);
const ANIM_2H = new Set([
  "slash_oversize",
  "slash_reverse_oversize",
  "thrust_oversize",
  "slash_128",
  "backslash_128",
  "halfslash_128",
  "thrust_128",
  "walk_128",
  "whip_oversize",
]);
const ANIM_STD_MELEE = new Set(["slash", "thrust"]);
const ANIM_RANGED = new Set(["shoot"]);

function itemIdFromPath(jsonPath) {
  const rel = path.relative(SHEET_DEF_DIR, jsonPath).replace(/\.json$/, "");
  return rel.split(path.sep).join("_");
}

async function walkJsonFiles(dir) {
  const out = [];
  const entries = await fs.readdir(dir, { withFileTypes: true });
  for (const entry of entries) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      out.push(...(await walkJsonFiles(full)));
    } else if (
      entry.name.endsWith(".json") &&
      !entry.name.startsWith("meta_")
    ) {
      out.push(full);
    }
  }
  return out;
}

function intersect(set, list) {
  return list.some((a) => set.has(a));
}

function classify(def) {
  const anims = def.animations ?? [];
  const tn = (def.type_name ?? "").toLowerCase();

  if (tn === "body") {
    // Body variants: classify by anim coverage breadth.
    const has1H = intersect(ANIM_1H, anims);
    return anims.length >= 12 ? "BodyFull" : has1H ? "BodyMid" : "BodyLimited";
  }

  if (tn.includes("shield") || tn.includes("ammo") || tn.includes("quiver")) {
    return "Shield";
  }
  if (tn.includes("tool")) return "Tool";

  const has1H = intersect(ANIM_1H, anims);
  const has2H = intersect(ANIM_2H, anims);
  const hasStd = intersect(ANIM_STD_MELEE, anims);
  const hasRanged = intersect(ANIM_RANGED, anims);

  if (hasRanged && !has2H) return "Ranged";
  if (has2H) return "2H";
  if (has1H || hasStd) return "1H";
  return "Unknown";
}

async function main() {
  const all = await walkJsonFiles(SHEET_DEF_DIR);
  // Classify weapons / shields / tools / bodies so the UI can badge each.
  const targets = all.filter(
    (p) =>
      p.includes(`${path.sep}weapons${path.sep}`) ||
      p.includes(`${path.sep}tools${path.sep}`) ||
      p.includes(`${path.sep}body${path.sep}`),
  );

  const byClass = {
    "1H": [],
    "2H": [],
    Ranged: [],
    Shield: [],
    Tool: [],
    BodyFull: [],
    BodyMid: [],
    BodyLimited: [],
    Unknown: [],
  };
  /** @type {Record<string, string>} */
  const map = {};

  for (const p of targets) {
    let def;
    try {
      def = JSON.parse(await fs.readFile(p, "utf8"));
    } catch {
      continue;
    }
    if (!def.name) continue;
    const itemId = itemIdFromPath(p);
    const cls = classify(def);
    map[itemId] = cls;
    byClass[cls].push({
      itemId,
      name: def.name,
      type_name: def.type_name,
      anims: def.animations ?? [],
    });
  }

  // --- Markdown ---
  const md = [`# Weapons / Shields / Tools Classification`, ``];
  md.push(
    `Generated ${new Date().toISOString()}. Scanned ${targets.length} sheet definitions.`,
  );
  md.push(``, `## Summary`);
  for (const k of [
    "1H",
    "2H",
    "Ranged",
    "Shield",
    "Tool",
    "BodyFull",
    "BodyMid",
    "BodyLimited",
    "Unknown",
  ]) {
    md.push(`- **${k}**: ${byClass[k].length}`);
  }
  md.push(``);

  for (const k of [
    "1H",
    "2H",
    "Ranged",
    "Shield",
    "Tool",
    "BodyFull",
    "BodyMid",
    "BodyLimited",
    "Unknown",
  ]) {
    const items = byClass[k];
    if (!items.length) continue;
    md.push(`## ${k} (${items.length})`);
    md.push(``);
    md.push(`| itemId | name | type | animations |`);
    md.push(`|---|---|---|---|`);
    for (const r of items.sort((a, b) => a.name.localeCompare(b.name))) {
      md.push(
        `| \`${r.itemId}\` | ${r.name} | ${r.type_name ?? "-"} | ${r.anims.join(", ")} |`,
      );
    }
    md.push(``);
  }

  await fs.writeFile(
    path.join(ROOT, "WEAPONS_CLASSIFICATION.md"),
    md.join("\n"),
  );
  console.log(`✓ WEAPONS_CLASSIFICATION.md`);

  // --- Data module ---
  const dataJs = `// AUTO-GENERATED by scripts/scan-weapon-classes.js. DO NOT EDIT.
// Maps weapon/shield/tool itemId → class for UI badges + random filtering.
// Classes: "1H" | "2H" | "Ranged" | "Shield" | "Tool" | "Unknown"

export const WEAPON_CLASS_BY_ID = ${JSON.stringify(map, null, 2)};

export const WEAPON_CLASSES_GENERATED_AT = ${JSON.stringify(new Date().toISOString())};

/** Quick helper: 1H/2H/Ranged/Shield/Tool/Unknown or null if unknown itemId. */
export function getWeaponClass(itemId) {
  return WEAPON_CLASS_BY_ID[itemId] ?? null;
}
`;
  await fs.writeFile(
    path.join(ROOT, "sources/state/weapon-classes-data.js"),
    dataJs,
  );
  console.log(`✓ sources/state/weapon-classes-data.js`);

  console.log(`\nSummary:`);
  for (const k of [
    "1H",
    "2H",
    "Ranged",
    "Shield",
    "Tool",
    "BodyFull",
    "BodyMid",
    "BodyLimited",
    "Unknown",
  ]) {
    console.log(`  ${k}: ${byClass[k].length}`);
  }
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
